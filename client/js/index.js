window.generateAndLogProof = async function(circuit) {
    document.querySelector("#proof").innerHTML = "fetching zkey/wasm...";
    let result = await prove(circuit);
    document.querySelector("#proof").innerHTML = JSON.stringify(result, null, 2);
    document.querySelector("#proof_time").innerHTML = `Proof Time: ${result.provingTime} ms`;
    document.querySelector("#witness_time").innerHTML = `Witness Gen Time: ${result.witGenTime} ms`;

    // verify it. 
    document.querySelector("#verified").innerHTML = "Verified: false";
}

const getKeys = async function(circuit) {
    const wasmUrl = new URL(`${circuit}.wasm`, `https://localhost:8090/build/${circuit}_js/`).toString();
    const zkeyUrl = new URL(`test_new_0001.zkey`, "https://localhost:8090/build/").toString();
    const wasm = await fetch(wasmUrl).then((r) => r.arrayBuffer());
    const zkey = await fetch(zkeyUrl).then((r) => r.arrayBuffer());
    return { wasm, zkey };
}

